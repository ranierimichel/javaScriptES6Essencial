// AND lógico (&&)

var a1 = true && true
var a2 = true && false
var a3 = false && true
var a4 = false && 3 == 4
var a5 = 'Gato' && 'Cão'
var a6 = false && 'Gato'
var a7 = 'Gato' && false

console.log(a1)
console.log(a2)
console.log(a3)
console.log(a4)
console.log(a5)
console.log(a6)
console.log(a7)

// OU lógico(||)

var o1 = true || true
var o2 = false || true
var o3 = true || false
var o4 = false || 3 == 4
var o5 = 'Gato' || 'Cão'
var o6 = false || 'Gato'
var o7 = 'Gato' || false

console.log(o1)
console.log(o2)
console.log(o3)
console.log(o4)
console.log(o5)
console.log(o6)
console.log(o7)

// Not lógico (!)

var n1 = !true
var n2 = !false
var n3 = !'Gato'

console.log(n1)
console.log(n2)
console.log(n3)

